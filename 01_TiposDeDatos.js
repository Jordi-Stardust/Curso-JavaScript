/** Tipos de datos
 */
// numero
console.log(27);
console.log(27.56);

// Texto - String
console.log("Jordi Romera");
console.log("Diego Romera");
console.log(`Hola, soy Jordi`);

// Boolean
console.log(true);
console.log(false);

// Undefined
console.log(undefined);

// Null. Aun no existe
console.log(null);

/**
 * Tipos de datos estructurales
 */

// Object
console.log({
  nombre: "Jordi",
  edad: 27,
  fechaDeNacimiento: null,
  esAlto: false,
});
console.log({
  nombre: "Jordi",
  edad: 27,
  fechaDeNacimiento: null,
  esAlto: false,
  ciudades: ["BCN", "MAD"],
  direccion: { callePrincipal: "Sant Agustí" },
});

//Array - Tipos de dato de tipo lista
console.log(["México", "Venezuela", "Ecuador"]);
console.log([21, true, { nombre: "Jordi" }, [1, 2, 3, 4, 5]]);
